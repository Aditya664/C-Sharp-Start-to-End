using System;
namespace IncreDecre 
{
    class IncreDecre 
    {
         //Increment and decrement operators
            //++ - increment operator
            // -- decrement operator
            //++ adds 1 ,-- subtract 1
            //they assign the new value to the variable.

        public static void Main(string[] args)
        {
           //increment operator
            int num = 4;
            num++;
            Console.WriteLine(num);
            //decrement operator
            int num1 = 5;
            num1--;
            Console.WriteLine(num1);

        }
    }
}