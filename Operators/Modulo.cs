using System;
namespace Modulo
{
    class Modulo
    {
        // What is Modulus operator?
        //     -the modulus operator is: %.
        //     -Gives us the reminder of the division.
        //     -5/4 gives us a reminder of 1 so 5 % 4 = 1.

        public static void Main(String[] args)
        {
            Console.WriteLine(5 % 4);
        }
    }
}